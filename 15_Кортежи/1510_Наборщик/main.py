def get_letter_position(letter, alphabet):
    for i, char in enumerate(alphabet):
        if char == letter:
            return i + 1
    return None


s = input()

unique_letters = set()

for char in s:
    if char in latin_alphabet or char in russian_alphabet:
        unique_letters.add(char)

for letter in unique_letters:
    if letter in latin_alphabet:
        position = get_letter_position(letter, latin_alphabet)
    elif letter in russian_alphabet:
        position = get_letter_position(letter, russian_alphabet)
    print(f"('{letter}', {position})")